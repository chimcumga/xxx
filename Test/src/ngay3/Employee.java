package ngay3;

public class Employee {
	public String  name;
	public int     id;
	public double  salary;
	public int     age;
	public boolean married;
	public Job     job;
	public Employee (String name, int id, double salary, int age, boolean married, Job job) {
		super();
		this.name = name;
		this.id = id;
		this.salary = salary;
		this.age = age;
		this.married = married;
		this.job = job;
	
}
}
